maxScore = 0

for i in maze:
    if i == 1:
        maxScore+= 1

def square(x, y):
    """Draw square using path at (x, y)."""
    path.up()
    path.goto(x, y)
    path.down()
    path.begin_fill()

    for count in range(4):
        path.forward(20)
        path.left(90)

    path.end_fill()

def offset(point):
    """Return offset of point in tiles."""
    x = (floor(point.x, 20) + 200) / 20
    y = (180 - floor(point.y, 20)) / 20
    index = int(x + y * 20)
    return index

def valid(point):
    """Return True if point is valid in tiles."""
    index = offset(point)
    if maze[index] == 0:
        return False

    index = offset(point + 19)
    if maze[index] == 0:
        return False
    
    is_in_column = point.y % tile_size == 0
    is_in_row = point.x % tile_size == 0
    return is_in_row or is_in_column

def world():
    """Draw world using path."""
    bgcolor('black')
    path.color('blue')

    for index in range(len(maze)):
        tile = maze[index]

        if tile > 0:
            x = (index % 20) * 20 - 200
            y = 180 - (index // 20) * 20
            square(x, y)

            if tile == 1:
                path.up()
                path.goto(x + 10, y + 10)
                path.dot(2, 'white')

def move():
    """Move pacman and all ghosts."""
    writer.undo()
    writer.write(state['score'], font = VERDANA_BOLD)

    clear()

    index = offset(pacman.position)

    if maze[index] == 1:
        maze[index] = empty_tile
        state['score'] += 1
        x = (index % 20) * 20 - 200
        y = 180 - (index // 20) * 20
        square(x, y)

    up()
    goto(pacman.position.x + 10, pacman.position.y + 10)
    dot(tile_size, 'yellow')

    if state['score'] == maxScore:
        end_game("You won!", "yellow")
        return

    for point, course in ghosts:
        if valid(point + course):
            point.move(course)
        else:
            options = [
                vector(5, 0),
                vector(-5, 0),
                vector(0, 5),
                vector(0, -5),
            ]
            plan = choice(options)
            course.x = plan.x
            course.y = plan.y

        up()
        goto(point.x + 10, point.y + 10)
        dot(tile_size, 'red')

    pacman.step(None)

    update()

    for point, course in ghosts:
        if abs(pacman.position - point) < 20:
            end_game("You lost!", "red")
            return

    ontimer(move, 50)

def end_game(message, tcolor):
    writer.penup()
    writer.goto(0, 180)
    writer.color(tcolor)
    writer.pendown()
    writer.write(message, align="center", font = VERDANA_BOLD)

